public class Motorcycle {
    private int compactParkingSize=1;
    private int regularParkingSize=1;
    private int largeParkingSize=1;

    public int getCompactParkingSize() {
        return compactParkingSize;
    }

    public void setCompactParkingSize(int compactParkingSize) {
        this.compactParkingSize = compactParkingSize;
    }

    public int getRegularParkingSize() {
        return regularParkingSize;
    }

    public void setRegularParkingSize(int regularParkingSize) {
        this.regularParkingSize = regularParkingSize;
    }

    public int getLargeParkingSize() {
        return largeParkingSize;
    }

    public void setLargeParkingSize(int largeParkingSize) {
        this.largeParkingSize = largeParkingSize;
    }


}
